export async function seedFabrics() {
 console.log('Seeding fabrics...')
 await Promise.all(
   fabricSeedData.map(async (fabric) => {
     const key = `fabric:${fabric.id}`
     await kv.hset(key, fabric)
   })
 )
 console.log(`Seeded ${fabricSeedData.length} fabrics.`)
}
